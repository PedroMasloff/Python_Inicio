#while: executa enquanto a condição for verdadeira.
#for: percorre elementos de uma sequência ou executa um bloco de código um certo número de vezes.

# Exemplo com while
contador = 0
while contador < 5:
    print("Contador:", contador)
    contador += 1

# Exemplo com for
nomes = ["Ana", "Carlos", "Bianca"]
for nome in nomes:
    print(nome)